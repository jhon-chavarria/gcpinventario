package ni.com.casapellas.gcpmcaja.pojos;


public class CuentaCaja  {


	private int Caja;
	private String CodigoCompania;
	private String Moneda;
	private String CodigoMetodoPago;
	private String MetodoPago;
	private String UnidadNegocio;
	private String Objeto;
	private String Subsidio;
	private String Estado;
	
	private String companyId;
	private String account;
	
	private String idCuentaPerdida;
	private String idCuentaIngreso;
	
	public String getCompanyId() {
		return companyId;
	}
	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getIdCuentaPerdida() {
		return idCuentaPerdida;
	}
	public void setIdCuentaPerdida(String idCuentaPerdida) {
		this.idCuentaPerdida = idCuentaPerdida;
	}
	public String getIdCuentaIngreso() {
		return idCuentaIngreso;
	}
	public void setIdCuentaIngreso(String idCuentaIngreso) {
		this.idCuentaIngreso = idCuentaIngreso;
	}
	public int getCaja() {
		return Caja;
	}
	public void setCaja(int caja) {
		Caja = caja;
	}
	public String getCodigoCompania() {
		return CodigoCompania;
	}
	public void setCodigoCompania(String codigoCompania) {
		CodigoCompania = codigoCompania;
	}
	public String getMoneda() {
		return Moneda;
	}
	public void setMoneda(String moneda) {
		Moneda = moneda;
	}
	public String getCodigoMetodoPago() {
		return CodigoMetodoPago;
	}
	public void setCodigoMetodoPago(String codigoMetodoPago) {
		CodigoMetodoPago = codigoMetodoPago;
	}
	public String getMetodoPago() {
		return MetodoPago;
	}
	public void setMetodoPago(String metodoPago) {
		MetodoPago = metodoPago;
	}
	public String getUnidadNegocio() {
		return UnidadNegocio;
	}
	public void setUnidadNegocio(String unidadNegocio) {
		UnidadNegocio = unidadNegocio;
	}
	public String getObjeto() {
		return Objeto;
	}
	public void setObjeto(String objeto) {
		Objeto = objeto;
	}
	public String getSubsidio() {
		return Subsidio;
	}
	public void setSubsidio(String subsidio) {
		Subsidio = subsidio;
	}
	public String getEstado() {
		return Estado;
	}
	public void setEstado(String estado) {
		Estado = estado;
	}
	
	
	
	
}
