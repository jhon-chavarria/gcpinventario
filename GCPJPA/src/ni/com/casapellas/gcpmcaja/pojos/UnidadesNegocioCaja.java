package ni.com.casapellas.gcpmcaja.pojos;

public class UnidadesNegocioCaja {
	private int noCaja;
	private String descripcionCaja;
	private String sucursalCaja; 
	private String unidadNegocio;
	private String estado;
	private String localizacion;
	
	public int getNoCaja() {
		return noCaja;
	}
	public void setNoCaja(int _noCaja) {
		noCaja = _noCaja;
	}
	public String getDescripcionCaja() {
		return descripcionCaja;
	}
	public void setDescripcionCaja(String _descripcionCaja) {
		descripcionCaja = _descripcionCaja;
	}
	public String getSucursalCaja() {
		return sucursalCaja;
	}
	public void setSucursalCaja(String _sucursalCaja) {
		sucursalCaja = _sucursalCaja;
	}
	public String getUnidadNegocio() {
		return unidadNegocio;
	}
	public void setUnidadNegocio(String _unidadNegocio) {
		unidadNegocio = _unidadNegocio;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String _estado) {
		estado = _estado;
	}
	public String getLocalizacion() {
		return localizacion;
	}
	public void setLocalizacion(String _localizacion) {
		localizacion = _localizacion;
	}
}