package ni.com.casapellas.gcpcxccobro.pojos;

public class CollectionDetail {

	private String activityCode;
	private String activityCodeName;
	private String comment;
	private String activityDate;
	private String activityTime;
	private int user;
	private String userName;
	
	public String getActivityCode() {
		return activityCode;
	}
	public void setActivityCode(String activityCode) {
		this.activityCode = activityCode;
	}
	public String getActivityCodeName() {
		return activityCodeName;
	}
	public void setActivityCodeName(String activityCodeName) {
		this.activityCodeName = activityCodeName;
	}
	public String getActivityDate() {
		return activityDate;
	}
	public void setActivityDate(String activityDate) {
		this.activityDate = activityDate;
	}
	public String getActivityTime() {
		return activityTime;
	}
	public void setActivityTime(String activityTime) {
		this.activityTime = activityTime;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public int getUser() {
		return user;
	}
	public void setUser(int user) {
		this.user = user;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	
	
	
}
