package ni.com.casapellas.gcpcxccobro.pojos;

public class DocumentsDetail {

	private String company;
	private String documenType;
	private String documentNumber;
	private String installment;
	private String requestNumber;
	private double amountCor;
	private double amountUsd;
	
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getDocumenType() {
		return documenType;
	}
	public void setDocumenType(String documenType) {
		this.documenType = documenType;
	}
	public String getDocumentNumber() {
		return documentNumber;
	}
	public void setDocumentNumber(String documentNumber) {
		this.documentNumber = documentNumber;
	}
	public String getInstallment() {
		return installment;
	}
	public void setInstallment(String installment) {
		this.installment = installment;
	}
	public String getRequestNumber() {
		return requestNumber;
	}
	public void setRequestNumber(String requestNumber) {
		this.requestNumber = requestNumber;
	}
	public double getAmountCor() {
		return amountCor;
	}
	public void setAmountCor(double amountCor) {
		this.amountCor = amountCor;
	}
	public double getAmountUsd() {
		return amountUsd;
	}
	public void setAmountUsd(double amountUsd) {
		this.amountUsd = amountUsd;
	}
	
	
	
	
}
