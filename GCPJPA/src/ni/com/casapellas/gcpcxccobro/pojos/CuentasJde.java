public class CuentasJde {
	private String codigoCuentaContable;
	private String codigoSucursal;
	private String codigoUnidadNegocio;
	private String cuentaObjeto;
	private String codigoSub;
	private String descripcionCuenta;
	private String numeroCuenta;
	
	public String getCodigoCuentaContable() {
		return codigoCuentaContable;
	}
	public void setCodigoCuentaContable(String codigoCuentaContable) {
		this.codigoCuentaContable = codigoCuentaContable;
	}
	public String getCodigoSucursal() {
		return codigoSucursal;
	}
	public void setCodigoSucursal(String codigoSucursal) {
		this.codigoSucursal = codigoSucursal;
	}
	public String getCodigoUnidadNegocio() {
		return codigoUnidadNegocio;
	}
	public void setCodigoUnidadNegocio(String codigoUnidadNegocio) {
		this.codigoUnidadNegocio = codigoUnidadNegocio;
	}
	public String getCuentaObjeto() {
		return cuentaObjeto;
	}
	public void setCuentaObjeto(String cuentaObjeto) {
		this.cuentaObjeto = cuentaObjeto;
	}
	public String getCodigoSub() {
		return codigoSub;
	}
	public void setCodigoSub(String codigoSub) {
		this.codigoSub = codigoSub;
	}
	public String getDescripcionCuenta() {
		return descripcionCuenta;
	}
	public void setDescripcionCuenta(String descripcionCuenta) {
		this.descripcionCuenta = descripcionCuenta;
	}
	public String getNumeroCuenta() {
		return numeroCuenta;
	}
	public void setNumeroCuenta(String numeroCuenta) {
		this.numeroCuenta = numeroCuenta;
	}
	
}
