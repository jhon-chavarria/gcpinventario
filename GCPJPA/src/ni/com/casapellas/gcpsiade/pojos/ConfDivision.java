package ni.com.casapellas.gcpsiade.pojos;

public class ConfDivision {
	private int id;
	private String name;
	private int sttaus;

	public ConfDivision() {
	}

	public int getSttaus() {
		return sttaus;
	}

	public void setSttaus(int sttaus) {
		this.sttaus = sttaus;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
