package ni.com.casapellas.gcptaller.pojo;

public class Stuff {
	private String CodCompania;
	private String CodTaller;
	private int CodOrden;
	private String CodArticulo;
	private String Cantidad;
	
	public String getCodCompania() {
		return CodCompania;
	}
	public void setCodCompania(String codCompania) {
		CodCompania = codCompania;
	}
	public String getCodTaller() {
		return CodTaller;
	}
	public void setCodTaller(String codTaller) {
		CodTaller = codTaller;
	}
	public int getCodOrden() {
		return CodOrden;
	}
	public void setCodOrden(int codOrden) {
		CodOrden = codOrden;
	}
	public String getCodArticulo() {
		return CodArticulo;
	}
	public void setCodArticulo(String codArticulo) {
		CodArticulo = codArticulo;
	}
	public String getCantidad() {
		return Cantidad;
	}
	public void setCantidad(String cantidad) {
		Cantidad = cantidad;
	}
	
}
