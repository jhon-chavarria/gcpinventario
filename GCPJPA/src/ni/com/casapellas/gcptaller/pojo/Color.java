package ni.com.casapellas.gcptaller.pojo;

public class Color {
	private String CodColor;
	private String Descripcion;
	private String Hexadicimal;
	private String Danio;
	private String Cono;
	private String Estado;
	
	public String getCodColor() {
		return CodColor;
	}
	public void setCodColor(String codColor) {
		CodColor = codColor;
	}
	public String getDescripcion() {
		return Descripcion;
	}
	public void setDescripcion(String descripcion) {
		Descripcion = descripcion;
	}
	public String getHexadicimal() {
		return Hexadicimal;
	}
	public void setHexadicimal(String hexadicimal) {
		Hexadicimal = hexadicimal;
	}
	public String getDanio() {
		return Danio;
	}
	public void setDanio(String danio) {
		Danio = danio;
	}
	public String getCono() {
		return Cono;
	}
	public void setCono(String cono) {
		Cono = cono;
	}
	public String getEstado() {
		return Estado;
	}
	public void setEstado(String estado) {
		Estado = estado;
	}
}
