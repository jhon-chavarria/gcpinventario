public class Legals {
	private String CodCompania;
	private String CodTaller;
	private String TipoCondicion;
	private String Titulo;
	private String Cuerpo;
	private String Pie;
	
	public String getCodCompania() {
		return CodCompania;
	}
	public void setCodCompania(String codCompania) {
		CodCompania = codCompania;
	}
	public String getCodTaller() {
		return CodTaller;
	}
	public void setCodTaller(String codTaller) {
		CodTaller = codTaller;
	}
	public String getTipoCondicion() {
		return TipoCondicion;
	}
	public void setTipoCondicion(String tipoCondicion) {
		TipoCondicion = tipoCondicion;
	}
	public String getTitulo() {
		return Titulo;
	}
	public void setTitulo(String titulo) {
		Titulo = titulo;
	}
	public String getCuerpo() {
		return Cuerpo;
	}
	public void setCuerpo(String cuerpo) {
		Cuerpo = cuerpo;
	}
	public String getPie() {
		return Pie;
	}
	public void setPie(String pie) {
		Pie = pie;
	}
}
