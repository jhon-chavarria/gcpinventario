package ni.com.casapellas.gcptaller.pojo;

public class Damage {
	private String CodDanio;
	private String Descripcion;
	private String CodElemento;
	private String CodColor;
	private String Desgastes;
	private String Estado;
	
	public String getCodDanio() {
		return CodDanio;
	}
	public void setCodDanio(String codDanio) {
		CodDanio = codDanio;
	}
	public String getDescripcion() {
		return Descripcion;
	}
	public void setDescripcion(String descripcion) {
		Descripcion = descripcion;
	}
	public String getCodElemento() {
		return CodElemento;
	}
	public void setCodElemento(String codElemento) {
		CodElemento = codElemento;
	}
	public String getCodColor() {
		return CodColor;
	}
	public void setCodColor(String codColor) {
		CodColor = codColor;
	}
	public String getDesgastes() {
		return Desgastes;
	}
	public void setDesgastes(String desgastes) {
		Desgastes = desgastes;
	}
	public String getEstado() {
		return Estado;
	}
	public void setEstado(String estado) {
		Estado = estado;
	}
}
