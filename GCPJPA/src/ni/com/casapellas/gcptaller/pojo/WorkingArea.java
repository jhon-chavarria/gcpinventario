package ni.com.casapellas.gcptaller.pojo;

public class WorkingArea {
	private String CodCompania;
	private String CodAreaTrabajo;
	private String Orden;
	private String Descripcion;
	private String Estado;
	
	
	public String getCodCompania() {
		return CodCompania;
	}
	public void setCodCompania(String codCompania) {
		CodCompania = codCompania;
	}
	public String getCodAreaTrabajo() {
		return CodAreaTrabajo;
	}
	public void setCodAreaTrabajo(String codAreaTrabajo) {
		CodAreaTrabajo = codAreaTrabajo;
	}
	public String getOrden() {
		return Orden;
	}
	public void setOrden(String orden) {
		Orden = orden;
	}
	public String getDescripcion() {
		return Descripcion;
	}
	public void setDescripcion(String descripcion) {
		Descripcion = descripcion;
	}
	public String getEstado() {
		return Estado;
	}
	public void setEstado(String estado) {
		Estado = estado;
	}
	
}
