package ni.com.casapellas.gcptaller.pojo;

public class ImageCoordinates {
	private String IdImage;
	private String CodAreaImage;
	private int PosicionX;
	private int PosicionY;
	private String CodElemento;
	private String Prioridad;
	private String Secuencia;
	private String Estado;
	private String Ubicacion;
	
	public String getIdImage() {
		return IdImage;
	}
	public void setIdImage(String idImage) {
		IdImage = idImage;
	}
	public String getCodAreaImage() {
		return CodAreaImage;
	}
	public void setCodAreaImage(String codAreaImage) {
		CodAreaImage = codAreaImage;
	}
	public int getPosicionX() {
		return PosicionX;
	}
	public void setPosicionX(int posicionX) {
		PosicionX = posicionX;
	}
	public int getPosicionY() {
		return PosicionY;
	}
	public void setPosicionY(int posicionY) {
		PosicionY = posicionY;
	}
	public String getCodElemento() {
		return CodElemento;
	}
	public void setCodElemento(String codElemento) {
		CodElemento = codElemento;
	}
	public String getPrioridad() {
		return Prioridad;
	}
	public void setPrioridad(String prioridad) {
		Prioridad = prioridad;
	}
	public String getSecuencia() {
		return Secuencia;
	}
	public void setSecuencia(String secuencia) {
		Secuencia = secuencia;
	}
	public String getEstado() {
		return Estado;
	}
	public void setEstado(String estado) {
		Estado = estado;
	}
	public String getUbicacion() {
		return Ubicacion;
	}
	public void setUbicacion(String ubicacion) {
		Ubicacion = ubicacion;
	}
}
