package ni.com.casapellas.gcptaller.pojo;

public class Chip {
	private String CodChip;
	private String DescChip;
	private String CodLineaProduccion;
	private String ColorHx; 
	private int DuracionChip;
	private String PorDefecto;
	private String EstadoChip;
	private String EstadoAsociacion;
	
	public String getCodChip() {
		return CodChip;
	}
	public void setCodChip(String codChip) {
		CodChip = codChip;
	}
	public String getDescChip() {
		return DescChip;
	}
	public void setDescChip(String descChip) {
		DescChip = descChip;
	}
	public String getCodLineaProduccion() {
		return CodLineaProduccion;
	}
	public void setCodLineaProduccion(String codLineaProduccion) {
		CodLineaProduccion = codLineaProduccion;
	}
	public String getColorHx() {
		return ColorHx;
	}
	public void setColorHx(String colorHx) {
		ColorHx = colorHx;
	}
	public int getDuracionChip() {
		return DuracionChip;
	}
	public void setDuracionChip(int duracionChip) {
		DuracionChip = duracionChip;
	}
	public String getPorDefecto() {
		return PorDefecto;
	}
	public void setPorDefecto(String porDefecto) {
		PorDefecto = porDefecto;
	}
	public String getEstadoChip() {
		return EstadoChip;
	}
	public void setEstadoChip(String estadoChip) {
		EstadoChip = estadoChip;
	}
	public String getEstadoAsociacion() {
		return EstadoAsociacion;
	}
	public void setEstadoAsociacion(String estadoAsociacion) {
		EstadoAsociacion = estadoAsociacion;
	}
	
}
