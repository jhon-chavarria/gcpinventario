package ni.com.casapellas.gcptaller.pojo;

public class CompanyInfo {
	private String CodCompania;
	private String CodTaller;
	private String Compania;
	private String Taller;
	private String Direccion;
	private String CiudadPais;
	private String ApartadoPostal;
	private String Telefono;
	private String LogoCompa;
	private String Moneda;
	private String UnidadNegocio;
	
	public String getCodCompania() {
		return CodCompania;
	}
	public void setCodCompania(String codCompania) {
		CodCompania = codCompania;
	}
	public String getCodTaller() {
		return CodTaller;
	}
	public void setCodTaller(String codTaller) {
		CodTaller = codTaller;
	}
	public String getCompania() {
		return Compania;
	}
	public void setCompania(String compania) {
		Compania = compania;
	}
	public String getTaller() {
		return Taller;
	}
	public void setTaller(String taller) {
		Taller = taller;
	}
	public String getDireccion() {
		return Direccion;
	}
	public void setDireccion(String direccion) {
		Direccion = direccion;
	}
	public String getCiudadPais() {
		return CiudadPais;
	}
	public void setCiudadPais(String ciudadPais) {
		CiudadPais = ciudadPais;
	}
	public String getApartadoPostal() {
		return ApartadoPostal;
	}
	public void setApartadoPostal(String apartadoPostal) {
		ApartadoPostal = apartadoPostal;
	}
	public String getTelefono() {
		return Telefono;
	}
	public void setTelefono(String telefono) {
		Telefono = telefono;
	}
	public String getLogoCompa() {
		return LogoCompa;
	}
	public void setLogoCompa(String logoCompa) {
		LogoCompa = logoCompa;
	}
	public String getMoneda() {
		return Moneda;
	}
	public void setMoneda(String moneda) {
		Moneda = moneda;
	}
	public String getUnidadNegocio() {
		return UnidadNegocio;
	}
	public void setUnidadNegocio(String unidadNegocio) {
		UnidadNegocio = unidadNegocio;
	}
	
}
