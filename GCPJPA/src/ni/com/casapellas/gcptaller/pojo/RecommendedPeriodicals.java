package ni.com.casapellas.gcptaller.pojo;

public class RecommendedPeriodicals {
	private String CodCompania;
	private String CodTaller;
	private String CodMarca;
	private String Modelo;
	private String Chasis;
	private String Combustible;
	private String CodActividad;
	private String Actividad;
	private String Justificacion;
	private Integer KilometroIni;
	private Integer KilometroFin;
	private String Estado;
	
	public String getActividad() {
		return Actividad;
	}
	public void setActividad(String actividad) {
		Actividad = actividad;
	}
	public String getJustificacion() {
		return Justificacion;
	}
	public void setJustificacion(String justificacion) {
		Justificacion = justificacion;
	}
	public String getCodCompania() {
		return CodCompania;
	}
	public void setCodCompania(String codCompania) {
		CodCompania = codCompania;
	}
	public String getCodTaller() {
		return CodTaller;
	}
	public void setCodTaller(String codTaller) {
		CodTaller = codTaller;
	}
	public String getCodMarca() {
		return CodMarca;
	}
	public void setCodMarca(String codMarca) {
		CodMarca = codMarca;
	}
	public String getModelo() {
		return Modelo;
	}
	public void setModelo(String modelo) {
		Modelo = modelo;
	}
	public String getChasis() {
		return Chasis;
	}
	public void setChasis(String chasis) {
		Chasis = chasis;
	}
	public String getCombustible() {
		return Combustible;
	}
	public void setCombustible(String combustible) {
		Combustible = combustible;
	}
	public String getCodActividad() {
		return CodActividad;
	}
	public void setCodActividad(String codActividad) {
		CodActividad = codActividad;
	}
	public Integer getKilometroIni() {
		return KilometroIni;
	}
	public void setKilometroIni(Integer kilometroIni) {
		KilometroIni = kilometroIni;
	}
	public Integer getKilometroFin() {
		return KilometroFin;
	}
	public void setKilometroFin(Integer kilometroFin) {
		KilometroFin = kilometroFin;
	}
	public String getEstado() {
		return Estado;
	}
	public void setEstado(String estado) {
		Estado = estado;
	}
}
