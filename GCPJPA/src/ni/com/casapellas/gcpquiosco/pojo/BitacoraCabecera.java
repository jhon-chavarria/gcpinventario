package ni.com.casapellas.gcpquiosco.pojo;



public class BitacoraCabecera {

	private Integer idBitacoraCabecera;
	private String tablaAfectada;
	private String operacion;
	private String usuario;
	private String fechaCreacion;
	private String horaCreacion;

	public Integer getIdBitacoraCabecera() {
		return idBitacoraCabecera;
	}
	public void setIdBitacoraCabecera(Integer _IdBitacoraCabecera) {
		idBitacoraCabecera = _IdBitacoraCabecera;
	}

	public String getTablaAfectada() {
		return tablaAfectada;
	}
	public void setTablaAfectada(String _TablaAfectada) {
		tablaAfectada = _TablaAfectada;
	}

	public String getOperacion() {
		return operacion;
	}
	public void setOperacion(String _Operacion) {
		operacion = _Operacion;
	}

	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String _Usuario) {
		usuario = _Usuario;
	}

	public String getFechaCreacion() {
		return fechaCreacion;
	}
	public void setFechaCreacion(String _FechaCreacion) {
		fechaCreacion = _FechaCreacion;
	}

	public String getHoraCreacion() {
		return horaCreacion;
	}
	public void setHoraCreacion(String _HoraCreacion) {
		horaCreacion = _HoraCreacion;
	}
 

	
	
}

