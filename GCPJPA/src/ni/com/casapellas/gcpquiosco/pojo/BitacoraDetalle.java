public class BitacoraDetalle {

	private Integer idBitacoraDetalle;
	private Integer idBitacoraCabecera;
	private String campoAfectado;
	private String valorDelCampo;

	public Integer getIdBitacoraDetalle() {
		return idBitacoraDetalle;
	}
	public void setIdBitacoraDetalle(Integer _IdBitacoraDetalle) {
		idBitacoraDetalle = _IdBitacoraDetalle;
	}

	public Integer getIdBitacoraCabecera() {
		return idBitacoraCabecera;
	}
	public void setIdBitacoraCabecera(Integer _IdBitacoraCabecera) {
		idBitacoraCabecera = _IdBitacoraCabecera;
	}

	public String getCampoAfectado() {
		return campoAfectado;
	}
	public void setCampoAfectado(String _CampoAfectado) {
		campoAfectado = _CampoAfectado;
	}

	public String getValorDelCampo() {
		return valorDelCampo;
	}
	public void setValorDelCampo(String _ValorDelCampo) {
		valorDelCampo = _ValorDelCampo;
	}
 

	
	
}

