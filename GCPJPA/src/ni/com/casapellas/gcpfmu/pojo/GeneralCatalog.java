package ni.com.casapellas.gcpfmu.pojo;

public class GeneralCatalog {
	private String id;
	private String name;
	private String orden;
	private String otros1;
	private String P25;
	private String description;
	private String phones;
	private String dgiCode;
	private String postal;

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	public String getOrden() {
		return orden;
	}
	public void setOrden(String orden) {
		this.orden = orden;
	}
	public String getOtros1() {
		return otros1;
	}
	public void setOtros1(String otros1) {
		this.otros1 = otros1;
	}
	public String getP25() {
		return P25;
	}
	public void setP25(String p25) {
		P25 = p25;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getPhones() {
		return phones;
	}
	public void setPhones(String phones) {
		this.phones = phones;
	}
	public String getDgiCode() {
		return dgiCode;
	}
	public void setDgiCode(String dgiCode) {
		this.dgiCode = dgiCode;
	}
	public String getPostal() {
		return postal;
	}
	public void setPostal(String postal) {
		this.postal = postal;
	}
	
}
